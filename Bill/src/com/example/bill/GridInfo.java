package com.example.bill;

public class GridInfo {
	
		private int img;
		private String type;
		public GridInfo(int img, String type) {
			super();
			this.img = img;
			this.type = type;
		}
		public int getImg() {
			return img;
		}
		public void setImg(int img) {
			this.img = img;
		}
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		
	}

